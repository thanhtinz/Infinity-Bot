// ---------------------------------------------------------------------------
// One-time (re-runnable) generator for the OtakuGifs reaction command batch.
//
// Reads src/bot/commands/otaku-reactions/definitions.js and writes one small
// hybrid command file per definition into src/bot/hybrid/<name>/<name>.js -
// the exact flat convention loadHybridCommands (src/bot/utils/
// commandLoader.js) expects: it only scans one level deep
// (`hybridPath/<dirName>/<dirName>.js`), so each reaction gets its own
// top-level directory directly under src/bot/hybrid/ (alongside e.g.
// hybrid/setprefix/setprefix.js, hybrid/nick/nick.js) rather than being
// nested under a shared "otaku-reactions" parent folder - nesting one level
// deeper would make loadHybridCommands silently skip every generated file.
//
// Each generated file just requires the shared factory
// (src/bot/utils/otakuReactionFactory.js) + its own definition and
// re-exports the resulting hybrid command object. Mirrors the approach used
// by scripts/generate-fun-commands.js for the fun-extra batch.
//
// Because these command directories live directly under the shared
// src/bot/hybrid/ folder (alongside many unrelated hand-written commands),
// stale-file cleanup below is scoped to only ever touch directories that
// contain a file with this script's own AUTO-GENERATED header - it will
// never remove a hybrid directory it didn't create.
//
// Usage: node scripts/generate-otaku-reactions.js
// ---------------------------------------------------------------------------

const fs = require('fs');
const path = require('path');

const definitions = require('../src/bot/commands/otaku-reactions/definitions.js');

const hybridDir = path.join(__dirname, '..', 'src', 'bot', 'hybrid');
const GENERATED_MARKER = '// AUTO-GENERATED by scripts/generate-otaku-reactions.js';

function fileHeader() {
  return [
    GENERATED_MARKER + ' - do not edit by hand.',
    '// Edit src/bot/commands/otaku-reactions/definitions.js and re-run the generator instead.',
    '',
  ].join('\n');
}

function buildFileContent(name) {
  return `${fileHeader()}const { createOtakuReactionCommand } = require('../../utils/otakuReactionFactory');
const definitions = require('../../commands/otaku-reactions/definitions');

const definition = definitions.find((entry) => entry.name === '${name}');

if (!definition) {
  throw new Error('otaku-reactions: missing definition for "${name}" - run scripts/generate-otaku-reactions.js again.');
}

module.exports = createOtakuReactionCommand(definition);
`;
}

function wasGeneratedByThisScript(dirName) {
  const filePath = path.join(hybridDir, dirName, `${dirName}.js`);
  if (!fs.existsSync(filePath)) return false;
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    return content.startsWith(GENERATED_MARKER);
  } catch {
    return false;
  }
}

function main() {
  if (!fs.existsSync(hybridDir)) {
    fs.mkdirSync(hybridDir, { recursive: true });
  }

  const seen = new Set();
  const generated = [];

  for (const definition of definitions) {
    if (!definition || !definition.name) continue;

    if (seen.has(definition.name)) {
      throw new Error(`Duplicate otaku-reactions definition name: ${definition.name}`);
    }
    seen.add(definition.name);

    const dir = path.join(hybridDir, definition.name);
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }

    const filePath = path.join(dir, `${definition.name}.js`);
    fs.writeFileSync(filePath, buildFileContent(definition.name), 'utf8');
    generated.push(definition.name);
  }

  // Remove stale generated directories for definitions that no longer exist.
  // Only ever touches directories this script itself generated (identified
  // by the AUTO-GENERATED header marker) - never an unrelated hybrid command.
  const existingDirs = fs.readdirSync(hybridDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name);

  let removed = 0;
  for (const dirName of existingDirs) {
    if (!seen.has(dirName) && wasGeneratedByThisScript(dirName)) {
      fs.rmSync(path.join(hybridDir, dirName), { recursive: true, force: true });
      removed++;
    }
  }

  console.log(`Generated ${generated.length} otaku-reactions command files in ${path.relative(process.cwd(), hybridDir)}`);
  if (removed > 0) {
    console.log(`Removed ${removed} stale generated director${removed === 1 ? 'y' : 'ies'}.`);
  }
}

main();
